package com.example.normal_2022.Model;

public class Ville {
    String label;
    String température;
    String précipitation;

    public String getLabel() {
        return label;
    }

    public void setLabel(String label) {
        this.label = label;
    }

    public String getTempérature() {
        return température;
    }

    public void setTempérature(String température) {
        this.température = température;
    }

    public String getPrécipitation() {
        return précipitation;
    }

    public void setPrécipitation(String précipitation) {
        this.précipitation = précipitation;
    }
}
